function showInput() {
  document.getElementById("extraInput").style.display = "block";
}

function hideInput() {
  document.getElementById("extraInput").style.display = "none";
}

function openModal() {
  var modal = document.getElementById("myModal");
  modal.style.display = "block";
}

function closeModal() {
  var modal = document.getElementById("myModal");
  modal.style.display = "none";
}
